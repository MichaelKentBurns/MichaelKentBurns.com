<?php

namespace CreativeMail\Models;

class User {

	/**
	 * User display name
	 *
	 * @var string
	 */
	public $display_name;
	/**
	 * User email address
	 *
	 * @var string
	 */
	public $email;
	/**
	 * User first name
	 *
	 * @var string
	 */
	public $first_name;
	/**
	 * User ID
	 *
	 * @var int
	 */
	public $id;
	/**
	 * User last name
	 *
	 * @var string
	 */
	public $last_name;
	/**
	 * User login username
	 *
	 * @var string
	 */
	public $username;
}
